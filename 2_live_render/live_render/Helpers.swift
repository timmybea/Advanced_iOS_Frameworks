import UIKit

struct ColorManager {
    
    static func backgroundGreen() -> UIColor {
        return UIColor(colorLiteralRed: 2/255, green: 51/255, blue: 31/255, alpha: 1)
    }
 
  
    static func ringGold() -> UIColor {
        return UIColor(colorLiteralRed: 207/255, green: 160/255, blue: 15/255, alpha: 1)
    }
}
